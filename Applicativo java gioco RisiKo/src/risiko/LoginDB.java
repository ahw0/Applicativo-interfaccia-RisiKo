    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // BOTTONE: LOGIN
     
        /* Prelevo user e password e li setto */
        ConnessioneDB.user = jTextField1.getText();
        ConnessioneDB.password = new String(jPasswordField1.getPassword());
        
        /* Effettuo la connessione al database */
        try{
                 ConnessioneDB.setDefaultConnection(ConnessioneDB.nuovaConnessione());
                 dispose();
                 new MenuBase().setVisible(true); // Mostro il menu principale
            } catch (SQLException e) 
                    {
                        Query.catturaErrori(e);
                    }
    }//GEN-LAST:event_jButton1ActionPerformed

