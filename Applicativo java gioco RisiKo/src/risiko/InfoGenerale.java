/* 
    DESCRIZIONE GENERALE

    Classe adibita per la visualizzazione di infomazioni generali riguardandi i valori presenti nel database.
    Tra questi sono mostrati:
    - Partite presenti: numero totale della partite create presenti nel database
    - Giocatori presenti: numero totale di giocatori presenti in tutte le partite
    - Dadi tirati: numero totale dei dadi che sono stati tirati in tutte le partite
    - Armate: numero totale di tutte le armate presenti in ogni partita
    - Turni di gioco: numero totale di tutti i turni che si sono svolti in ogni partita
    - Combattimenti:  numero totale di combattimenti svolti
    - Posizionamenti: numero totale di posizionamenti svolti
    - Spostamenti: numero totale di spostamenti svolti
    - Preliminari: numero totale di tutti gli inserimenti preliminari che sono stati fatti
    - Classici: numero totale di tutti gli inserimenti classici (inserimenti derivati dal turno cassico) che sono stati fatti
    - Combinazione carte: numero totale di tutti gli inserimenti tramite l'uso di una combinazione di carte che sono stati fatti
    - Territorio preferito dai giocatori: viene calcolato prendendo tutti i territori di ogni partita e scegliendo quello con numero di armate
                                          maggiore. Nel caso in cui siano presenti più territorio con stesso numero di armate, viene scelto sempre
                                          uno tra questi
    - Territorio più trascurato: medesimo discorso di sopra ma con il minor numero di armate presenti
    - Territorio più attaccato: medesimo discorso solo che si verifica in base ai combattimenti il numero più grande dei combattimenti svolti in un dato territorio
    - Combinazione carte più utilizzata: mostra la combinazione di carte più usata dai giocatori di tutte le partite presenti
    - Obiettivo più presente: obiettivo più presente
*/
