    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // APERTURA FORM: INFO GENERALE
        
        Query dbms = new Query();
        int var = 0;
        
        /* Partite presenti */
        var = dbms.InfoGenerPartita();
        jTextField1.setText(String.valueOf(var));
       
        /* Giocatori presenti */
        var = dbms.InfoGenerGiocatori();
        jTextField2.setText(String.valueOf(var));
        
        /* Dadi tirati */
        var = dbms.InfoGenerDadi();
        jTextField4.setText(String.valueOf(var));
        
        /* Armate presenti */
        var = dbms.InfoGenerArmate();
        jTextField5.setText(String.valueOf(var));
        
        /* Turni presenti */
        var = dbms.InfoGenerTurni();
        jTextField6.setText(String.valueOf(var));
        
        /* Combattimenti presenti */
        var = dbms.InfoGenerCombattimenti();
        jTextField14.setText(String.valueOf(var));
        
        /* Posizionamenti presenti */
        var = dbms.InfoGenerPosizionamenti();
        jTextField7.setText(String.valueOf(var));
        
        /* Spostamenti presenti */
        var = dbms.InfoGenerSpostamenti();
        jTextField8.setText(String.valueOf(var));
        
        /* Fasi preliminari presenti */
        var = dbms.InfoGenerPreliminari();
        jTextField10.setText(String.valueOf(var));
        
        /* Inserimenti classici presenti */
        var = dbms.InfoGenerClassici();
        jTextField11.setText(String.valueOf(var));
        
        /* Inserimenti combinazione carte presenti */
        var = dbms.InfoGenerCarte();
        jTextField12.setText(String.valueOf(var));
        
        /* Territorio più apprezzato (quello con numero max di carri) */
        var = dbms.InfoGenerTerrPiu();
        String nomet = dbms.nomeTerritorio(String.valueOf(var));
        if(nomet != null)
            jTextField9.setText(nomet);
        else
            jTextField9.setText("Nessun territorio");
        
        /* Territorio meno apprezzato (quello con numero min di carri) */
        var = dbms.InfoGenerTerrMeno();
        nomet = dbms.nomeTerritorio(String.valueOf(var));
        if(nomet != null)
            jTextField13.setText(nomet);
        else
            jTextField13.setText("Nessun territorio");
       
        
       /* Territorio più attaccati */
       var = dbms.InfoGeneraleTerrPiu();
       nomet = dbms.nomeTerritorio(String.valueOf(var));
       if(nomet != null)
            jTextField17.setText(nomet);
        else
            jTextField17.setText("Nessun territorio");
       
       /* Obiettivo più presente */
       String ob = dbms.InfoGeneraleOb();
       if(ob != null)
           jTextField15.setText(ob);
        else
           jTextField15.setText("Nessun obiettivo");
        
       /* Combinazione di carte più usata */
       var = dbms.InfoGeneraleTipoPos();
       if(var != 0)
           MostraCombinazione(var);
        else
           jTextField16.setText("Nessuna combinazione");  
    }//GEN-LAST:event_formWindowOpened

 /* Funzione adibita per mostrare la combinazione più usata in base al numero ottenuto interrogando il database */
 public void MostraCombinazione(int comb)
 {
     switch(comb)
     {
         case 2:
             jTextField16.setText("3 Cavalieri (CCC)");
             break;
         case 3:
             jTextField16.setText("3 Fanti (FFF)");
             break; 
         case 4:
             jTextField16.setText("3 Artiglieri (AAA)");
             break;
         case 5:
             jTextField16.setText("Fante - Cavaliere - Artigliere (FCA)");
             break;
         case 6:
             jTextField16.setText("Jolly - Cavaliere - Cavaliere (JCC)");
             break;
         case 7:
            jTextField16.setText("Jolly - Fante - Fante (JFF)");
             break;
         case 8:
            jTextField16.setText("Jolly - Artigliere - Artigliere (JAA)");
            break;       
     }
 }

